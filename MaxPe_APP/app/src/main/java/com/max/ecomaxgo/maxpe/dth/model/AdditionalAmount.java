package com.max.ecomaxgo.maxpe.dth.model;

public class AdditionalAmount {
    String name;
    String value;

    public AdditionalAmount(String name, String value) {
        this.name = name;
        this.value = value;
    }

    public String getName() {
        return name;
    }

    public String getValue() {
        return value;
    }
}
